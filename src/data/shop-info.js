// Генерация текстового описания для системного промпта
export function generateShopContext() {
  const { stores, deliverySamara, deliveryRemote, deliveryFeatures, payment, notes } = shopInfo;

  let context = `ИНФОРМАЦИЯ О МАГАЗИНЕ "${shopInfo.name}" (${shopInfo.city}):\n\n`;

  // Адреса
  context += `АДРЕСА МАГАЗИНОВ:\n`;
  stores.forEach(s => {
    context += `- ${s.address} / ${s.hours}\n`;
  });

  // Доставка по Самаре
  context += `\nДОСТАВКА ПО САМАРЕ:\n`;
  context += `Минимальная сумма заказа: ${deliverySamara.minOrder} рублей\n`;
  context += `Районы: ${deliverySamara.districts.join(', ')}\n`;
  context += `Интервал доставки: ${deliverySamara.interval}\n`;
  context += `Стоимость:\n`;
  deliverySamara.prices.forEach(p => {
    context += `- ${p.period}: ${p.price}\n`;
  });

  // Отдалённые районы
  context += `\nДОСТАВКА В ОТДАЛЁННЫЕ РАЙОНЫ:\n`;
  deliveryRemote.zones.forEach(z => {
    context += `- ${z.areas.join(', ')}: ${z.price}\n`;
  });

  // Особенности
  context += `\nОСОБЕННОСТИ:\n`;
  deliveryFeatures.forEach(f => {
    context += `- ${f}\n`;
  });

  // Оплата
  context += `\nСПОСОБЫ ОПЛАТЫ:\n`;
  payment.forEach(p => {
    context += `- ${p}\n`;
  });

  // Примечания
  if (notes.length > 0) {
    context += `\nДОПОЛНИТЕЛЬНО:\n`;
    notes.forEach(n => {
      context += `- ${n}\n`;
    });
  }

  return context;
}
